
def addition(number1,number2):
    return(number1 + number2)


def subtraction(number1,number2):
    if(number1 > number2):
        return number1 - number2
    else:
        return "For subtraction number1 should be greater then number2"

def multiplication(number1,number2):
    return number1 * number2


def division(number1,number2):
     if(number2 > 0):
         return(number1 / number2)
     else:
        return "To apply division number2 should be greater then 0"



